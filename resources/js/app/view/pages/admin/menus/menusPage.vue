<template>
    <div>
        <div class="mt-3">
            <label>
                <router-link to="/">{{$t('Home')}}</router-link>
            </label>
            <span>/</span>
            <label active>{{$t('Menus')}}</label>
        </div>
        <h4 class="mt-3">{{$t('Menus')}}</h4>
        <div class="main-card mt-3 card">
            <div class="card-body">
                <div>
                    <div class="row">
                        <div class="col-md-6">
                            <span id="addWrapper">
                                <button  class="btn mb-2 btn-transition btn-outline-primary"
                                         @click='addNewItem()'>
                                    {{$t("AddNew")}}
                                </button>

                                <download-excel
                                    class = "btn mb-2 mr-2 btn-transition btn-outline-success"
                                    :data   = "items"
                                    :fields = "lang =='ar'?itemsExportAr: itemsExport"
                                    :worksheet = "$t('Menus')"
                                    name= "kitchens.xls"
                                >
                                    {{$t('ExportExcel')}}
                                </download-excel>

                                <button  class="btn mb-2 btn-transition btn-outline-info"
                                         @click='openFilters()'>
                                    {{$t("Filters")}}
                                </button>

                                <button  class="btn mb-2 btn-transition btn-outline-danger"
                                         @click='clearFilters()'>
                                    {{$t("ClearFilters")}}
                                </button>


                            </span>
                        </div>
                    </div>
                    <b-table show-empty
                             :empty-filtered-text="$t('emptyTable')"
                             :empty-text="$t('emptyTable')"
                             stacked="md"
                             :bordered="true"
                             :striped="true"
                             :items="items"
                             :fields="fields"
                             :per-page="pagination.perPage"
                             :filter="filter"
                             :filterIncludedFields="filterOn"
                             :sort-by.sync="sortBy"
                             :sort-desc.sync="sortDesc"
                             :sort-direction="sortDirection"
                             @filtered="onFiltered"
                    >
                        <template v-slot:cell(status)="row">
                            <span v-if="row.item.status" style="color: green; font-weight: bold">
                                {{$t('Active')}}
                                <div @click="updateStatus(row.item)"
                                     :style="row.item.status?'background:green': 'background:red'"
                                     style="display:inline-block;cursor:pointer;width:10px;height:10px;border-radius:50%;">
                                </div>
                            </span>
                            <span v-else style="color: red; font-weight: bold">
                                {{$t('InActive')}}
                                 <div @click="updateStatus(row.item)"
                                      :style="row.item.status?'background:green': 'background:red'"
                                      style="display:inline-block;cursor:pointer;width:10px;height:10px;border-radius:50%;">
                                </div>
                                </span>
                        </template>
                        <template v-slot:cell(actions)="row" >
                            <b-dropdown no-flip :text='$t("Actions")' class="" variant="primary">
                                <span :id="'editWrapper'+row.index">
                                    <button type="button"  class="dropdown-item text-primary"
                                            @click='editRow(row.item.id)'>
                                        {{$t("Edit")}}
                                    </button>
                                </span>
                                <span :id="'deleteWrapper'+row.index">
                                    <button type="button"  class="dropdown-item text-danger"
                                            @click='deleteRow(row.item.id)'>
                                        {{$t("Delete")}}
                                    </button>
                                </span>
                                <span :id="'detailsWrapper'+row.index">
                                    <button type="button"  class="dropdown-item text-success"
                                            @click='detailsRow(row.item.id)'>
                                        {{$t("Details")}}
                                    </button>
                                </span>
                            </b-dropdown>
                        </template>
                    </b-table>
                    <div class="row">
                        <div class="col-md-6">
                            <b-pagination
                                v-model="pagination.currentPage"
                                :total-rows="pagination.total"
                                :per-page="pagination.perPage"
                                @change="paginationChange"
                                class="my-0"
                            >
                            </b-pagination>
                        </div>
                    </div>
                </div>
            </div>
        </div>
        <el-drawer
            :title="$t('Filters')"
            :visible.sync="filterDrawer"
            :direction="'ltr'"
            :size="'20%'"
        >
            <div class="row">
                <div class="col-md-1"></div>
                <div class="col-md-10">
                    <div class="form-group"
                    >
                        <label class="control-label">{{$t("Name")}}</label>
                        <input type="text" name="name"
                               class="form-control"
                               v-model="filters.name"
                        >
                    </div>
                </div>
            </div>
            <div class="row">
                <div class="col-md-1"></div>
                <div class="col-md-10">
                    <b-button @click="filterData" variant="success">{{$t('Filter')}}</b-button>
                    <b-button @click="closeFilters" variant="secondary">{{$t('Close')}}</b-button>
                </div>
            </div>
        </el-drawer>
    </div>
</template>
<script>
export default {
    data() {
        return {
            filters: {
                page: null
            },
            lang: localStorage.getItem("lang") || 'ar',
            itemsExport: {

            },
            itemsExportAr: {
            },
            filterDrawer: false,
            items: [],
            fields: [
                { key: 'cooking_date', label: this.$t('cooking_date'), sortable: true, sortDirection: 'desc' },
                { key: 'actions', label: '','thStyle':'width:10%;','class':'action-column' }
            ],
            pagination:{
                currentPage: 1,
                perPage:  10,
            },
            sortBy: '',
            sortDesc: false,
            sortDirection: 'asc',
            filter: null,
            filterOn: [],
            filteredItems: [],
            editMode: false,
            viewMode: false,
        }
    },
    methods: {
        clearFilters() {
            this.filters = {
                page: null
            }
            this.initData(this.filters);
        },
        openFilters() {
            this.filterDrawer= true;
        },
        closeFilters() {
            this.filterDrawer= false;
        },
        filterData() {
            this.pagination.currentPage= 1;
            this.filters.page = 1;
            this.initData(this.filters);
            this.closeFilters();
        },
        deleteRow(idVal){
            this.$confirm(this.$t('DeleteConfirmMessage'), this.$t('DeleteConfirmTitle'), {
                confirmButtonText: this.$t('DeleteConfirmOk'),
                cancelButtonText: this.$t('DeleteConfirmCancel'),
                type: 'warning'
            }).then(() => {
                this.$store.dispatch("menus/removeData", idVal)
                    .then(_ => {
                        this.updateData(null);
                        this.$notify.success({
                            duration: 3000,
                            message: this.$t("DeleteSuccessfully"),
                            title: this.$t("Delete"),
                            customClass: "top-center",
                        });
                    })
                    .catch(_=>{
                        this.$notify.error({
                            duration: 3000,
                            message: this.$t("DeleteFailed"),
                            title: this.$t("Delete"),
                            customClass: "top-center",
                        });
                    })

            })
        },
        onFiltered(filteredItems) {
            this.pagination.currentPage = 1;
            this.filteredItems = filteredItems;
        },
        initData(filters) {
            this.$store
                .dispatch("menus/getData", filters)
                .then(res => {

                    this.items = res.data.resources;
                    this.pagination.from = res.data.pagination.from;
                    this.pagination.to = res.data.pagination.to;
                    this.pagination.total = res.data.pagination.total;
                })
                .catch(_ => {
                    this.$notify.error({
                        duration: 3000,
                        message: this.$t("GetDataFailed"),
                        title: this.$t("GetData"),
                        customClass: "top-center",
                    });
                })
        },
        addNewItem:function(){
            this.$router.push({name: 'menusAddEdit'})
        },
        detailsRow(id) {
            this.$router.push({name: 'menusAddEdit', params: {id: id, page: 'details'}})
        },
        editRow(id) {
            this.$router.push({name: 'menusAddEdit', params: {id: id, page: 'edit'}})
        },
        updateData(){
            this.initData(null);
        },
        updateStatus(data) {
            let status = null;
            if (data.status == 1) status = 0;
            else status = 1;
            let sendData = {
                ids: data.id + "",
                status: status,
            };
            this.$store
                .dispatch("menus/updateStatus", JSON.stringify(sendData))
                .then(() => {
                    this.$notify.success({
                        duration: 3000,
                        message: this.$t("UpdatedSuccessfully"),
                        title: this.$t("Update"),
                        customClass: "top-center",
                    });
                    this.initData(this.filters);
                })

                .catch((error) => {
                    this.$notify.error({
                        duration: 3000,
                        message: error,
                        title: this.$t("Error"),
                        customClass: "top-center",
                    });
                });
        },
        paginationChange(page) {
            this.filters.page = page;
            this.initData(this.filters);
        },
    },
    created() {
        this.initData(null);
    }
}
</script>
